export function setLangCookie(lang){
	document.cookie = `lang=${lang}; path=/`;
}

export function getLangCookie(){
	if (!document.cookie){
		return null;
	}
	const cookies = document.cookie.split(';');
	for (const cookie of cookies){
		const [name, value] = cookie.trim().split('=');
		if (name === 'lang'){
			return value || "EN";
		}
	}
	return null;
}

export function translate(lang){
	const elements = Object.keys(translations[lang]);
    elements.forEach(selector => {
        const element = document.querySelector(`.${selector}`);
		if (!element) {
			const elementID = document.getElementById(selector);
			if (elementID) {
				if (selector === "dashboard-score-historic-text"){
					const textContent = elementID.textContent;
					let translated;
					if (lang === "FR" && textContent.includes("WINS")) 
					{
						translated = textContent
						.replace("WINS", translations[lang][selector]["WINS"])
						.replace("LOSSES", translations[lang][selector]["LOSSES"]);
					} else if (lang === "FR" && textContent.includes("VICTORIAS"))
					{
						translated = textContent
						.replace("VICTORIAS", translations[lang][selector]["WINS"])
						.replace("DERROTAS", translations[lang][selector]["LOSSES"]);
					}
					else if (lang === "ES" && textContent.includes("WINS")){
						translated = textContent
						.replace("WINS", translations[lang][selector]["WINS"])
						.replace("LOSSES", translations[lang][selector]["LOSSES"]);
					}
					else if (lang === "ES" && textContent.includes("GAGNÉES"))
					{
						translated = textContent
						.replace("GAGNÉES", translations[lang][selector]["WINS"])
						.replace("PERDUES", translations[lang][selector]["LOSSES"]);
					}
					else if (lang === "EN" && textContent.includes("GAGNÉES"))
					{
						translated = textContent
						.replace("GAGNÉES", translations[lang][selector]["WINS"])
						.replace("PERDUES", translations[lang][selector]["LOSSES"]);
					} else if (lang === "EN" && textContent.includes("VICTORIAS"))
					{
						translated = textContent
						.replace("VICTORIAS", translations[lang][selector]["WINS"])
						.replace("DERROTAS", translations[lang][selector]["LOSSES"]);
					}
					elementID.textContent = translated;
					return;
				} else if (selector === "dashboard-subtitle-match-history"){
					const textContent = elementID.textContent;
					if (lang === "EN" && textContent.includes("MATCHS JOUÉS"))
					{
						const translated = textContent.replace("MATCHS JOUÉS", translations[lang][selector]["MATCHES PLAYED"]);
						elementID.textContent = translated;
						return;
					} else if (lang === "FR" && textContent.includes("MATCHES PLAYED"))
					{
						const translated = textContent.replace("MATCHES PLAYED", translations[lang][selector]["MATCHES PLAYED"]);
						elementID.textContent = translated;
						return;
					} else if (lang === "ES" && textContent.includes("MATCHES PLAYED"))
					{
						const translated = textContent.replace("MATCHES PLAYED", translations[lang][selector]["MATCHES PLAYED"]);
						elementID.textContent = translated;
						return;
					} else if (lang === "ES" && textContent.includes("MATCHS JOUÉS"))
					{
						const translated = textContent.replace("MATCHS JOUÉS", translations[lang][selector]["MATCHES PLAYED"]);
						elementID.textContent = translated;
						return;
					} else if (lang === "FR" && textContent.includes("PARTIDOS JUGADOS"))
					{
						const translated = textContent.replace("PARTIDOS JUGADOS", translations[lang][selector]["MATCHES PLAYED"]);
						elementID.textContent = translated;
						return;
					} else if (lang === "EN" && textContent.includes("PARTIDOS JUGADOS"))
					{
						const translated = textContent.replace("PARTIDOS JUGADOS", translations[lang][selector]["MATCHES PLAYED"]);
						elementID.textContent = translated;
						return;
					}
					const translated = textContent.replace("MATCHES PLAYED", translations[lang][selector]["MATCHES PLAYED"]);
					elementID.textContent = translated;
					return;
				}
				elementID.textContent = translations[lang][selector];
				if (elementID.placeholder)
					elementID.placeholder = translations[lang][selector];
			}
		}
        else if (element) {
            element.textContent = translations[lang][selector];
		}
		else {
			console.log(`Element ${selector} not found`);
		}
	});
}